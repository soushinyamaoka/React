// 学習データから分類を検索
function classifyByLearned(name, learnedCategories) {
  if (!learnedCategories || Object.keys(learnedCategories).length === 0) return null;

  const normalized = normalizeName(name);
  if (!normalized) return null;

  // 1. 完全一致（正規化後）
  if (learnedCategories[normalized]) return learnedCategories[normalized];

  // 2. 部分一致（学習キーが商品名に含まれる or 商品名が学習キーに含まれる）
  //    長いキーから順にチェック（より具体的なものを優先）
  const sortedKeys = Object.keys(learnedCategories).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (key.length >= 4 && (normalized.includes(key) || key.includes(normalized))) {
      return learnedCategories[key];
    }
  }

  return null;
}

// メイン分類関数
// learnedCategories: { normalizedName: category, ... }
export function classifyItem(name, learnedCategories) {
  // 1. 学習データで分類（最優先）
  const learned = classifyByLearned(name, learnedCategories);
  if (learned) return learned;

  // 2. キーワードルールで分類（半角カナ→全角カナ変換してからマッチ）
  const prepared = prepareForMatch(name);
  for (const rule of CATEGORY_RULES) {
    if (rule.keywords.some((kw) => prepared.includes(kw.toLowerCase()))) {
      return rule.category;
    }
  }
  return 'その他';
}

