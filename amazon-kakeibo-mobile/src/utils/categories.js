// 名前を正規化（学習マッチング用）
function normalizeName(name) {
  return (name || '')
    .toLowerCase()
    .replace(/[\s　\t\u3000]/g, '')
    .replace(/[（）()「」『』【】\[\]]/g, '')
    .replace(/…+/g, '')
    .replace(/[,、。.・]/g, '')
    .replace(/\d{4}[-\/]\d{1,2}[-\/]\d{1,2}/g, '') // 日付除去
    .substring(0, 40);
}

// 半角カナ→全角カナ変換（SMBC明細用）
function hanToZenKana(str) {
  const kanaMap = {
    'ｱ':'ア','ｲ':'イ','ｳ':'ウ','ｴ':'エ','ｵ':'オ',
    'ｶ':'カ','ｷ':'キ','ｸ':'ク','ｹ':'ケ','ｺ':'コ',
    'ｻ':'サ','ｼ':'シ','ｽ':'ス','ｾ':'セ','ｿ':'ソ',
    'ﾀ':'タ','ﾁ':'チ','ﾂ':'ツ','ﾃ':'テ','ﾄ':'ト',
    'ﾅ':'ナ','ﾆ':'ニ','ﾇ':'ヌ','ﾈ':'ネ','ﾉ':'ノ',
    'ﾊ':'ハ','ﾋ':'ヒ','ﾌ':'フ','ﾍ':'ヘ','ﾎ':'ホ',
    'ﾏ':'マ','ﾐ':'ミ','ﾑ':'ム','ﾒ':'メ','ﾓ':'モ',
    'ﾔ':'ヤ','ﾕ':'ユ','ﾖ':'ヨ',
    'ﾗ':'ラ','ﾘ':'リ','ﾙ':'ル','ﾚ':'レ','ﾛ':'ロ',
    'ﾜ':'ワ','ｦ':'ヲ','ﾝ':'ン',
    'ｧ':'ァ','ｨ':'ィ','ｩ':'ゥ','ｪ':'ェ','ｫ':'ォ',
    'ｯ':'ッ','ｬ':'ャ','ｭ':'ュ','ｮ':'ョ',
    'ﾞ':'゛','ﾟ':'゜','ｰ':'ー',
  };
  let result = '';
  for (let i = 0; i < str.length; i++) {
    const ch = str[i];
    const next = str[i + 1];
    if (kanaMap[ch]) {
      let zen = kanaMap[ch];
      // 濁点・半濁点の結合
      if (next === 'ﾞ' || next === '゛') {
        const dakuten = { 'カ':'ガ','キ':'ギ','ク':'グ','ケ':'ゲ','コ':'ゴ','サ':'ザ','シ':'ジ','ス':'ズ','セ':'ゼ','ソ':'ゾ','タ':'ダ','チ':'ヂ','ツ':'ヅ','テ':'デ','ト':'ド','ハ':'バ','ヒ':'ビ','フ':'ブ','ヘ':'ベ','ホ':'ボ','ウ':'ヴ' };
        if (dakuten[zen]) { zen = dakuten[zen]; i++; }
      } else if (next === 'ﾟ' || next === '゜') {
        const handakuten = { 'ハ':'パ','ヒ':'ピ','フ':'プ','ヘ':'ペ','ホ':'ポ' };
        if (handakuten[zen]) { zen = handakuten[zen]; i++; }
      }
      result += zen;
    } else {
      result += ch;
    }
  }
  return result;
}

// 分類用にテキストを前処理（半角カナ→全角カナ + lowercase）
function prepareForMatch(name) {
  const zenKana = hanToZenKana(name || '');
  return zenKana.toLowerCase();
}

